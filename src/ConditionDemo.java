public class ConditionDemo {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        if(a == b){
            System.out.println(1);
            System.out.println(2);
            System.out.println(3);
            System.out.println(4);

        } else if ( a > b) {
            System.out.println("FALSE");
        } else {
            System.out.println("TRUE/FALSE");
        }
    }
}
