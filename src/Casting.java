
public class Casting {

	public static void main(String[] args) {
		
		double b = 1;
		
		System.out.println(b);
		 
		double d = 1.1;
		System.out.println(d);
		int e = (int) 1.1;
		System.out.println(e);
		
		// 1 to String
		String f = Integer.toString(1);
		System.out.println(f.getClass());

		System.out.println("aaa\n");

	}

}
